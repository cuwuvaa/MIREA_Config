# Инициализация вектора длины 4
LOAD_CONST 10     # Загрузить константу 10
WRITE_MEM 0       # Записать в память по адресу 0

LOAD_CONST -5     # Загрузить константу -5
WRITE_MEM 1       # Записать в память по адресу 1

LOAD_CONST 0      # Загрузить константу 0
WRITE_MEM 2       # Записать в память по адресу 2

LOAD_CONST 7      # Загрузить константу 7
WRITE_MEM 3       # Записать в память по адресу 3

# Применение операции sgn() к каждому элементу
LOAD_CONST 0      # Устанавливаем аккумулятор в 0 (адрес первого элемента)
UNARY_SGN         # Выполняем sgn(память[0]), результат в аккумуляторе
WRITE_MEM 0       # Записываем результат обратно в адрес 0

LOAD_CONST 1      # Устанавливаем аккумулятор в 1 (адрес второго элемента)
UNARY_SGN         # Выполняем sgn(память[1])
WRITE_MEM 1       # Записываем результат обратно в адрес 1

LOAD_CONST 2      # Адрес третьего элемента
UNARY_SGN         # sgn(память[2])
WRITE_MEM 2       # Записываем результат обратно в адрес 2

LOAD_CONST 3      # Адрес четвертого элемента
UNARY_SGN         # sgn(память[3])
WRITE_MEM 3       # Записываем результат обратно в адрес 3
